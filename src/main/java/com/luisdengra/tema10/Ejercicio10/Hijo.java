package com.luisdengra.tema10.Ejercicio10;

public class Hijo {
    /**
     * Atributos
     */
    String nombre;
    int edad;

    public Hijo(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }
}
